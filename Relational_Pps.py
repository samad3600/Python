# Define two variables
x = 10
y = 20

# Equal to (==)
print(f"{x} == {y} is {x == y}")

# Not equal to (!=)
print(f"{x} != {y} is {x != y}")

# Greater than (>)
print(f"{x} > {y} is {x > y}")

# Less than (<)
print(f"{x} < {y} is {x < y}")

# Greater than or equal to (>=)
print(f"{x} >= {y} is {x >= y}")

# Less than or equal to (<=)
print(f"{x} <= {y} is {x <= y}")
