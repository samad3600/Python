# Addition
result_add = 10 + 5
print("Addition:", result_add)

# Subtraction
result_sub = 15 - 7
print("Subtraction:", result_sub)

# Multiplication
result_mul = 3 * 4
print("Multiplication:", result_mul)

# Division
result_div = 20 / 4
